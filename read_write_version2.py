# 讀取CSV資料
with open(file_path, "r", encoding="utf-8") as f:
    reader = csv.reader(f)
    next(reader)  # 跳過 header

    for row in reader:
        if not row:
            continue
        row_dict = {}
        for i in range(2, len(row) - 1, 3):
            try:
                rssi_db = int(row[i])
                if( rssi_db >= -10):
                    continue
                else:
                    power_mW = 10 ** (rssi_db / 10)
                    time = row[i + 1]
                    row_dict[time] = power_mW
            except (ValueError, IndexError):
                continue
        data_list.append(row_dict)

